public class T2 {
	private String id;
	private String y;
	private String z;
	private String zz;

	public T2(String id, String y, String z, String zz) {
		this.id = id;
		this.y = y;
		this.z = z;
		this.zz = zz;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getY() {
		return y;
	}

	public void setY(String y) {
		this.y = y;
	}

	public String getZ() {
		return z;
	}

	public void setZ(String z) {
		this.z = z;
	}

	public String getZz() {
		return zz;
	}

	public void setZz(String zz) {
		this.zz = zz;
	}

}
